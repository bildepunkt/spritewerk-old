/**
 * create 2D point
 *
 * @class Vector
 */
export default class Vector {
    /**
     * [constructor description]
     * @param  {[type]} x [description]
     * @param  {[type]} y [description]
     * @return {[type]}   [description]
     */
    constructor(x, y) {
        this.x = x || 0;
        this.y = y || 0;
    }
}
